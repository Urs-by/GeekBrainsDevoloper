def scheme(field: list):
    """
    Схема игрового поля
    :return: игровое поле с данными
    """
    return f"┌─────┬─────┬─────┐\n" \
           f"|  {field[0]}  |  {field[1]}  |  {field[2]}  |\n" \
           f"├─────┼─────┼─────┤\n" \
           f"|  {field[3]}  |  {field[4]}  |  {field[5]}  |\n" \
           f"├─────┼─────┼─────┤\n" \
           f"|  {field[6]}  |  {field[7]}  |  {field[8]}  |\n" \
           f"└─────┴─────┴─────┘"